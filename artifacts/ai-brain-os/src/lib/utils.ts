import { clsx, type ClassValue } from "clsx"
import { twMerge } from "tailwind-merge"

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs))
}

// Returns the user's local calendar date as YYYY-MM-DD (not UTC). Using
// `new Date().toISOString()` for "today" is a common source of off-by-one-day
// bugs near midnight because it reports the UTC date, not the viewer's date.
export function getLocalDateString(date: Date = new Date()): string {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}
